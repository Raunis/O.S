package modelo;

public class TipoDeEquipamento {
	private int idTipoDeEquipamento;
	private String nomeTipoDeEquipamento;
	private String descricao;

	public int getIdTipoDeEquipamento() {
		return idTipoDeEquipamento;
	}

	public void setIdTipoDeEquipamento(int idTipoDeEquipamento) {
		this.idTipoDeEquipamento = idTipoDeEquipamento;
	}

	public String getNomeTipoDeEquipamento() {
		return nomeTipoDeEquipamento;
	}

	public void setNomeTipoDeEquipamento(String nomeTipoDeEquipamento) {
		this.nomeTipoDeEquipamento = nomeTipoDeEquipamento;
	}

	public String getDescricao() {
		return descricao;
	}

	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}

}
